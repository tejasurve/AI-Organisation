// Drop-in sanitiser for text that has to render through PDFKit's built-in
// WinAnsi (CP1252) Type-1 fonts. Maps the most common "looks-like-WinAnsi
// but isn't" code points (arrows, ellipsis, emoji, severity blocks) to their
// ASCII fallbacks so the glyphs render correctly across PDF viewers and so
// that test extraction of the produced text is deterministic.
//
// Anything outside ASCII + the WinAnsi Latin-1 set + the explicit CP1252
// 0x80-0x9F extras is replaced with '?' rather than silently dropped — that
// way agent-supplied text that contains exotic characters surfaces visibly
// in the PDF rather than corrupting the surrounding glyphs.
const WIN_ANSI_REPLACEMENTS: Record<string, string> = {
  "\u2190": "<-",
  "\u2191": "^",
  "\u2192": "->",
  "\u2193": "v",
  "\u21D0": "<=",
  "\u21D1": "^^",
  "\u21D2": "=>",
  "\u21D3": "vv",
  "\u2026": "...",
  "\u2713": "v",
  "\u2714": "v",
  "\u2717": "x",
  "\u2718": "x",
  "\u2705": "[OK]",
  "\u274C": "[X]",
  "\u26A0": "[!]",
  "\uFE0F": "",
  "\u23F1": "",
  "\u2728": "",
  "\uD83D\uDFE5": "[critical]",
  "\uD83D\uDFE7": "[high]",
  "\uD83D\uDFE1": "[medium]",
  "\uD83D\uDFE2": "[low]",
};

const WIN_ANSI_EXTRAS = new Set<number>([
  0x20ac, 0x201a, 0x0192, 0x201e, 0x2020, 0x2021, 0x02c6, 0x2030, 0x0160,
  0x2039, 0x0152, 0x017d, 0x2018, 0x2019, 0x201c, 0x201d, 0x2022, 0x2013,
  0x2014, 0x02dc, 0x2122, 0x0161, 0x203a, 0x0153, 0x017e, 0x0178,
]);

export function pdfSafe(s: string | null | undefined): string {
  if (!s) return "";
  let out = "";
  for (let i = 0; i < s.length; ) {
    let ch: string;
    const code = s.charCodeAt(i);
    if (code >= 0xd800 && code <= 0xdbff && i + 1 < s.length) {
      // UTF-16 surrogate pair (e.g. emoji)
      ch = s.slice(i, i + 2);
      i += 2;
    } else {
      ch = s[i];
      i += 1;
    }
    if (Object.prototype.hasOwnProperty.call(WIN_ANSI_REPLACEMENTS, ch)) {
      out += WIN_ANSI_REPLACEMENTS[ch];
      continue;
    }
    const cp = ch.codePointAt(0)!;
    if (cp <= 0x7e) {
      out += ch;
      continue;
    }
    if (cp >= 0xa0 && cp <= 0xff) {
      out += ch;
      continue;
    }
    if (WIN_ANSI_EXTRAS.has(cp)) {
      out += ch;
      continue;
    }
    out += "?";
  }
  return out;
}
