function drawTableHeader(
  doc: PDFKit.PDFDocument,
  cells: ReadonlyArray<string>,
  cols: ReadonlyArray<{ x: number; width: number; align: "left" | "right" }>,
): void {
  ensureSpace(doc, 26);
  const y = doc.y;
  const totalWidth = cols.reduce((s, c) => s + c.width, 0);
  doc.save();
  doc.fillColor(COLORS.panelStrong);
  doc.rect(cols[0].x, y, totalWidth, 22).fill();
  doc.restore();
  doc.save();
  doc.strokeColor(COLORS.border).lineWidth(0.5);
  doc.moveTo(cols[0].x, y + 22).lineTo(cols[0].x + totalWidth, y + 22).stroke();
  doc.restore();
  doc.font("Helvetica-Bold").fontSize(9.5).fillColor(COLORS.primaryDark);
  for (let i = 0; i < cells.length; i++) {
    const col = cols[i];
    doc.text(cells[i], col.x + 6, y + 6, {
      width: col.width - 12,
      align: col.align,
      ellipsis: true,
    });
  }
  doc.y = y + 22;
  doc.x = PAGE.margin.left;
}

function drawTableRow(
  doc: PDFKit.PDFDocument,
  cells: ReadonlyArray<string>,
  cols: ReadonlyArray<{ x: number; width: number; align: "left" | "right" }>,
  opts?: {
    striped?: boolean;
    cellColors?: ReadonlyArray<string | undefined>;
    cellFonts?: ReadonlyArray<"code" | undefined>;
  },
): void {
  // Compute the natural row height by measuring the tallest cell.
  doc.font("Helvetica").fontSize(9.5);
  let rowHeight = 18;
  for (let i = 0; i < cells.length; i++) {
    const col = cols[i];
    const isCode = opts?.cellFonts?.[i] === "code";
    doc.font(isCode ? "Courier" : "Helvetica").fontSize(isCode ? 9 : 9.5);
    const h = doc.heightOfString(cells[i] || " ", { width: col.width - 12, align: col.align }) + 8;
    if (h > rowHeight) rowHeight = h;
  }
  ensureSpace(doc, rowHeight + 4);
  const y = doc.y;
  const totalWidth = cols.reduce((s, c) => s + c.width, 0);

  if (opts?.striped) {
    doc.save();
    doc.fillColor(COLORS.panel);
    doc.rect(cols[0].x, y, totalWidth, rowHeight).fill();
    doc.restore();
  }
  doc.save();
  doc.strokeColor(COLORS.border).lineWidth(0.5);
  doc.moveTo(cols[0].x, y + rowHeight).lineTo(cols[0].x + totalWidth, y + rowHeight).stroke();
  doc.restore();

  for (let i = 0; i < cells.length; i++) {
    const col = cols[i];
    const isCode = opts?.cellFonts?.[i] === "code";
    const color = opts?.cellColors?.[i] ?? COLORS.text;
    doc.font(isCode ? "Courier" : "Helvetica").fontSize(isCode ? 9 : 9.5).fillColor(color);
    doc.text(cells[i] || "", col.x + 6, y + 5, {
      width: col.width - 12,
      align: col.align,
      ellipsis: true,
      lineGap: 1,
    });
  }
  doc.y = y + rowHeight;
  doc.x = PAGE.margin.left;
}

function drawJsonBlock(doc: PDFKit.PDFDocument, label: string, body: unknown): void {
  drawSubheading(doc, label);
  // JSON is rendered through a built-in WinAnsi monospace, so it has the same
  // glyph-availability constraints as the prose text. Sanitise after stringify
  // so structure is preserved and only individual character glyphs are mapped.
  const json = pdfSafe(JSON.stringify(body, null, 2));
  const lineHeight = 11;
  const padding = 10;
  // Reserve a little space for the open of the box
  ensureSpace(doc, lineHeight * 4 + padding * 2);

  // We let PDFKit auto-paginate the text; the panel background is drawn under
  // the text block as a single rectangle on each page it spans. Easiest: draw
  // a single panel for the first segment, then a continuation panel on each
  // additional page using the page-add hook.
  const startPageIndex = currentPageIndex(doc);

  // Pre-render text into a temp buffer to know per-page slices? PDFKit doesn't
  // expose that. Instead: emit text + on every pageAdded during emission, draw
  // the panel from top-of-page down to where the text continues. This is tricky.
  //
  // Pragmatic alternative: just draw the JSON text in monospace with a faint
  // left rule rather than a full panel. Easier to reason about across pages.
  doc.font("Courier").fontSize(8).fillColor(COLORS.text);
  const startY = doc.y;
  const lineX = PAGE.margin.left;
  // Left guide bar
  doc.save();
  doc.fillColor(COLORS.code);
  doc.rect(lineX, startY, 3, Math.min(lineHeight * 6, PAGE_BOTTOM - startY)).fill();
  doc.restore();
  doc.text(json, PAGE.margin.left + 12, startY, {
    width: CONTENT_WIDTH - 12,
    lineGap: 1,
    indent: 0,
  });
  doc.x = PAGE.margin.left;
  // Mark the unused variable so the linter doesn't complain.
  void startPageIndex;
}

function drawRoundedRect(doc: PDFKit.PDFDocument, x: number, y: number, w: number, h: number, r: number): PDFKit.PDFDocument {
  const radius = Math.min(r, w / 2, h / 2);
  doc
    .moveTo(x + radius, y)
    .lineTo(x + w - radius, y)
    .quadraticCurveTo(x + w, y, x + w, y + radius)
    .lineTo(x + w, y + h - radius)
    .quadraticCurveTo(x + w, y + h, x + w - radius, y + h)
    .lineTo(x + radius, y + h)
    .quadraticCurveTo(x, y + h, x, y + h - radius)
    .lineTo(x, y + radius)
    .quadraticCurveTo(x, y, x + radius, y);
  return doc;
}

function ensureSpace(doc: PDFKit.PDFDocument, neededHeight: number): void {
  const available = PAGE_BOTTOM - doc.y;
  if (available < neededHeight) doc.addPage();
}

function currentPageIndex(doc: PDFKit.PDFDocument): number {
  const range = doc.bufferedPageRange();
  return range.start + range.count - 1;
}

// ---------- footers (called after all content is emitted) ----------

function addFootersToAllPages(doc: PDFKit.PDFDocument, footer: string, now: Date): void {
  const range = doc.bufferedPageRange();
  const total = range.count;
  for (let i = range.start; i < range.start + total; i++) {
    doc.switchToPage(i);
    const pageNum = i - range.start + 1;
    // Skip page 1 (the cover) so it stays clean — start numbering from page 2.
    if (pageNum === 1) continue;
    drawFooter(doc, footer, now, pageNum, total);
  }
}

function drawFooter(doc: PDFKit.PDFDocument, footer: string, now: Date, pageNum: number, total: number): void {
  const y = PAGE.height - PAGE.margin.bottom + 24;
  // Hairline rule
  doc.save();
  doc.strokeColor(COLORS.border).lineWidth(0.5);
  doc.moveTo(PAGE.margin.left, y - 8).lineTo(CONTENT_RIGHT, y - 8).stroke();
  doc.restore();

  const leftWidth = CONTENT_WIDTH * 0.6;
  doc.font("Helvetica").fontSize(8).fillColor(COLORS.textMuted);
  doc.text(footer, PAGE.margin.left, y, { width: leftWidth, align: "left" });
  doc.text(
    `Page ${pageNum} of ${total}  ·  ${now.toISOString()}`,
    PAGE.margin.left + leftWidth,
    y,
    { width: CONTENT_WIDTH - leftWidth, align: "right" },
  );
}

